function renderProjectReadme({ slug, workflowSummary }) {
  return `# ${slug}

Generated by the Data Visualization Kit.

## Current Workflow State

- Owner workflow: ${workflowSummary.ownerWorkflow}
- Active visualization path: ${workflowSummary.tool}
- Primary methodology: ${workflowSummary.framework}

## Project Workspace Rule

All project-specific docs, plans, and user assets stay under \`docs/\`.
`;
}

function renderProjectGitignore() {
  return `.env
.env.*
.venv/
__pycache__/
*.pyc
dist/
build/
`;
}

function renderProjectBriefDoc({ slug, intake }) {
  return `# Project Brief

## Identity

- Slug: ${slug}
- Context: ${intake.projectContext}
- Dataset: ${intake.projectDataset}
- Goals: ${intake.projectGoals}

## Intake Contract

- Mandatory intake was completed
- This project should continue through \`$dv-*\` workflows only
`;
}

function renderProjectPlanDoc({ decisions }) {
  const pythonRule = decisions.layer.requiresPython ? "required" : "optional";
  return `# Project Plan

## Decisions

- Framework: ${decisions.framework.name}
- Visualization layer: ${decisions.layer.name}
- Visualization tool: ${decisions.tool.name}
- SQL: required
- Python: ${pythonRule}

## Recommended Flow

1. run \`$dv-cook\` for the full post-intake project workflow
2. rerun \`$dv-data-preparation\` or \`$dv-data-visualize\` only for targeted refreshes
3. run \`$dv-document-management\` when project docs need focused cleanup
4. run \`$dv-debug\` only when needed
`;
}

function renderDataPreparationDoc({ intake, decisions }) {
  const pythonStep = decisions.layer.requiresPython
    ? "3. write Python cleaning or transformation helpers"
    : "3. Python is optional for this project";
  return `# Data Preparation

## Goal

Prepare clean, validated data aligned to the project goals.

## Inputs

- Dataset: ${intake.projectDataset}
- Context: ${intake.projectContext}

## Planned Steps

1. inspect source shape and quality risks
2. write SQL profiling and transformation queries
${pythonStep}
4. document schema assumptions and caveats
`;
}

