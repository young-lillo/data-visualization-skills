function renderPublishDoc({ slug, decisions, preferFreeDeploy }) {
  const deployTarget = decisions.deployTarget ?? "VPS";
  const useStaticDeploy = decisions.tool.name === "Evidence";
  const deploySection =
    useStaticDeploy
      ? `## Static Deploy (${deployTarget})

- Run \`npm run sources && npm run build\` inside \`projects/${slug}/evidence/\`
- Publish directory: \`build\`
- Env var pattern: \`EVIDENCE_SOURCE__[source_name]__[option]\`
- Config file: \`${deployTarget === "Netlify" ? "netlify.toml" : "vercel.json"}\` (generated by \`$dv-publish\`)
`
      : `## Server Deploy (${deployTarget})

- Confirm Docker Compose or deployment manifest is present
- Verify DB connection credentials are in env vars, not source files
- Metabase: minimum 2GB RAM; Superset: minimum 4GB RAM
`;

  return `# Publish

## Goal

Make \`${slug}\` ready for git and deploy.

## Publish Notes

- Prefer free deploy: ${preferFreeDeploy ? "Yes" : "No"}
- Visualization tool: ${decisions.tool.name}
- Deploy target: ${deployTarget}
- Deployment guidance: ${decisions.tool.deployNote}

${deploySection}
## Checklist

1. validate docs completeness
2. confirm project workspace is commit-ready
3. confirm deploy config file exists for ${deployTarget}
4. verify no credentials in source files
`;
}

